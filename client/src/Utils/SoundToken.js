class SoundToken
{
    constructor(soundToken) {
        this.name = soundToken.name;
        this.description = soundToken.description;
        this.creator = soundToken.creator;
        this.soundURL = soundToken.soundURL;
        this.metadataURL = soundToken.metadataURL;
        this.collectable = soundToken.collectable;
        this.amount = soundToken.amount;
        this.tokenID = soundToken.tokenID;
        this.holder = soundToken.holder;
        this.author = soundToken.author;
    }
}